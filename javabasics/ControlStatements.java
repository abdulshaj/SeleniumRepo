package javabasics;

public class ControlStatements {
	
	public static void main(String[] args) {
		for(int i=1;i<=20;i++) {
			if(i%2 != 0) {
				System.out.println(i);
			}
		}
		
	String browser = "edge";
		
	switch (browser) {
	case "firefox":
		System.out.println("firefox");
		break;
	case "edge":
		System.out.println("edge");
		break;
	
	}
	}

}
